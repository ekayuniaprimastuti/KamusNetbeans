package com.example.myapplication2;

public class Data {
    private String eng;
    private String ind;

    public Data() {
    }

    public Data(String eng, String ind) {
        this.eng = eng;
        this.ind = ind;
    }

    public String getEng() {
        return eng;
    }

    public void setEng(String eng) {
        this.eng = eng;
    }

    public String getInd() {
        return ind;
    }

    public void setInd(String ind) {
        this.ind = ind;
    }
}
